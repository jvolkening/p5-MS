=head1 NAME

MS - Namespace for mass spectrometry-related modules

=head1 DESCRIPTION

The MS namespace is intended to serve as a top-level home for Perl modules related to
mass spectrometry. Currently existing modules are scattered about under
various namespaces depending on their specific use. It is the hope of the
authors that future development will take place under this namespace, with the
hierarchy expanding as development continues.

This package contains a number of core modules for working with mass
spectrometry data. See the POD for individual modules for further details.

